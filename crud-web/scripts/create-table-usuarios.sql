CREATE TABLE `projeto-crud-1.0`.`usuarios` ( `id` INT NOT NULL AUTO_INCREMENT , 
											`nome` VARCHAR(45) NOT NULL , 
											`sobrenome` VARCHAR(45) NOT NULL , 
											`login` VARCHAR(45) NOT NULL , 
											`senha` VARCHAR(45) NOT NULL , 
											PRIMARY KEY (`id`)) 
											ENGINE = InnoDB;
											
